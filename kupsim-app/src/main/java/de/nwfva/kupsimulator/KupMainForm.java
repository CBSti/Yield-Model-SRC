    private StandGen initStandGen() {

        String outputDir = configuration.getConfiguration().getString("simulator.config.output.folder");
        String configDir = configuration.getConfiguration().getString("simulator.config.dir");
        String configFile = configuration.getConfiguration().getString("simulator.config.settings");
        String modelRegion = configuration.getConfiguration().getString("simulator.config.region");

        return new StandGen(outputDir, configDir, configFile, modelRegion, logger);
    }

    private void ensureOutputFolderIsAvailable() {

        String outputDir = configuration.getConfiguration().getString("simulator.config.output.folder");
        Path outputDirPath = Paths.get(outputDir);

        File file = outputDirPath.toFile();
        boolean createdDir = file.mkdirs();

        logger.info("Created output diretory {}", createdDir);
    }

    private String getOutputFile(String filename) {

        String outputDir = configuration.getConfiguration().getString("simulator.config.output.folder");
        Path outputDirPath = Paths.get(outputDir, filename);

        File file = outputDirPath.toFile();
        return file.getAbsolutePath();
    }


    private void startSimulation() {

        TreegrossXML2 xmlParser = new TreegrossXML2();

        String clone = (String) jComboBox1.getSelectedItem();
        double awc = Double.parseDouble(jTextField1.getText());
        int doyPlant = Integer.parseInt(jTextField2.getText());
        double prec = Double.parseDouble(jTextField3.getText());
        double temp = Double.parseDouble(jTextField4.getText());
        int bz = Integer.parseInt(jTextField5.getText());

        st = this.standGen.standGen(clone, awc, doyPlant, prec, temp, bz);
        this.dataCompletion.completeData(st);

        xmlParser.saveAsXML(st, getOutputFile("standYear1.xml"));

        // YEAR 2
        this.growth.grow(st);
        this.dataCompletion.completeData(st);

        xmlParser.saveAsXML(st, getOutputFile("standYear2.xml"));

        // YEAR 3
        this.growth.grow(st);
        this.dataCompletion.completeData(st);

        jLabel6.setText(summary.getSummaryString(st));


        xmlParser.saveAsXML(st, getOutputFile("standYear3.xml"));


        // Year 4 Resprout after harvest
        this.growth.resprout(st);

        // Data completion year 4
        this.dataCompletion.completeData(st);

        xmlParser.saveAsXML(st, getOutputFile("standYear4.xml"));


        // Year 5
        this.growth.grow(st);
        this.dataCompletion.completeData(st);


        xmlParser.saveAsXML(st, getOutputFile("standYear5.xml"));

        // Year 6
        this.growth.grow(st);
        this.dataCompletion.completeData(st);



        jLabel11.setText(summary.getSummaryString(st));


        xmlParser.saveAsXML(st, getOutputFile("standYear6.xml"));
    }

